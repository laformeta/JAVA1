package lesson01;

public class Quiz01 {

	public static void main(String[] args) {
		String name = "김성훈";
		int age = 28;
		int three = 3;
		int five = 5;
		int seven = 7;
		int one = 1;
		int two = 2;
		
		System.out.println("이름 : " + name + " 나이 : " + age);
		System.out.println();
		System.out.println(three + " + " + five + " = " + (three + five));
		System.out.println(three + " - " + five + " = " + (three - five));
		System.out.println();
		System.out.println(seven + " X " + one + " = " + (seven * one));
		System.out.println(seven + " X " + two + " = " + (seven * two));
		System.out.println(seven + " X " + three + " = " + (seven * three));
	}

}
